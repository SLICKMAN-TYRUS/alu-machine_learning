#!/usr/bin/env python3
"""Module for array concatenation"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays

    Args:
        arr1: First array (list of ints/floats)
        arr2: Second array (list of ints/floats)

    Returns:
        A new list containing all elements from arr1 followed by arr2
    """
    return arr1 + arr2
